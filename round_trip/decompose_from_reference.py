"""Decompose a working library into ADRs + contracts + oracles.

Phase 1 (this file): stub that writes a single placeholder ADR so the
downstream gates + recomposer have something to read. Makes ZERO LLM
calls.

Phase 2: real prompt that walks `repo_dir` source files and emits
faithful ADRs/contracts/oracles under `output_dir/<lib>/`. Uses
LLMClient from `benchmarks/commit0/baselines/_llm.py` with ephemeral
caching on the source-tree block (cached across files in one library).

Run standalone:
    python benchmarks/round_trip/decompose_from_reference.py wcwidth
"""

from __future__ import annotations

import argparse
import sys
import time
from pathlib import Path

# Reuse the commit0 baseline's LLM client + cost helper. `benchmarks/`
# isn't a proper package (and `commit0` isn't importable as a module
# name), so follow the same sys.path insertion pattern as
# kaizen_delta.py.
_COMMIT0_BASELINES = (
    Path(__file__).resolve().parent.parent / "commit0" / "baselines"
)
if str(_COMMIT0_BASELINES) not in sys.path:
    sys.path.insert(0, str(_COMMIT0_BASELINES))

# Phase 2 will need these; keep the import so the wiring is proven.
from _llm import LLMClient, cost, DEFAULT_MODELS  # noqa: E402,F401


DEFAULT_REPOS_ROOT = Path.home() / "kaizen-commit0" / "repos"


def _write_text(path: Path, text: str) -> None:
    """UTF-8 + newline='' — Windows→Linux-container safe."""
    path.parent.mkdir(parents=True, exist_ok=True)
    if not text.endswith("\n"):
        text += "\n"
    path.write_text(text, encoding="utf-8", newline="")


def decompose_from_reference(
    lib: str,
    output_dir: Path,
    provider: str = "anthropic",
    model: str = "claude-sonnet-4-6",
    repos_root: Path | None = None,
) -> dict:
    """Emit `output_dir/<lib>/{adrs,contracts,oracles}/` from a working lib.

    Phase 1: writes a single placeholder ADR. Zero LLM calls.
    Returns: {"cost_usd", "elapsed_s", "files_in_spec"}.
    """
    t0 = time.time()
    repos_root = repos_root or DEFAULT_REPOS_ROOT
    repo_dir = repos_root / lib  # referenced for symmetry with phase 2

    spec_dir = output_dir / lib
    (spec_dir / "adrs").mkdir(parents=True, exist_ok=True)
    (spec_dir / "contracts").mkdir(parents=True, exist_ok=True)
    (spec_dir / "oracles").mkdir(parents=True, exist_ok=True)

    _write_text(
        spec_dir / "adrs" / "ADR-0001-placeholder.md",
        "# ADR-0001 (placeholder)\n\n"
        f"Generated by phase-1 scaffold for `{lib}`. Real decompose "
        "prompt lands in phase 2.\n\n"
        f"Source tree (not read in phase 1): `{repo_dir.as_posix()}`\n",
    )

    return {
        "cost_usd": 0.0,
        "elapsed_s": round(time.time() - t0, 3),
        "files_in_spec": 1,
    }


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument("lib", help="commit0-lite library name (e.g., wcwidth)")
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path(__file__).resolve().parent / "spec",
        help="Root directory for spec/<lib>/. Default: benchmarks/round_trip/spec/",
    )
    parser.add_argument("--provider", choices=["anthropic", "openai"], default="anthropic")
    parser.add_argument("--model", default=None)
    args = parser.parse_args()

    model = args.model or DEFAULT_MODELS[args.provider]
    result = decompose_from_reference(args.lib, args.output_dir, args.provider, model)
    print(f"decompose {args.lib}: {result}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
