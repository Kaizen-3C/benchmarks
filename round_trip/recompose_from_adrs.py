"""Recompose code from ADRs + contracts + oracles (no source peeking).

Phase 1 (this file): stub that writes a single placeholder Python file
under `output_dir/<lib>/` so the downstream metrics have something to
read. Makes ZERO LLM calls.

Phase 2: real generator — reads only `spec/<lib>/{adrs,contracts,oracles}/`
and emits a complete source tree. The recomposer MUST NOT see the
original implementation (that would rig the round-trip; see
benchmarks/round_trip/PLAN.md "Recomposer view of ADRs: strict").

Run standalone:
    python benchmarks/round_trip/recompose_from_adrs.py wcwidth
"""

from __future__ import annotations

import argparse
import sys
import time
from pathlib import Path

_COMMIT0_BASELINES = (
    Path(__file__).resolve().parent.parent / "commit0" / "baselines"
)
if str(_COMMIT0_BASELINES) not in sys.path:
    sys.path.insert(0, str(_COMMIT0_BASELINES))

from _llm import LLMClient, cost, DEFAULT_MODELS  # noqa: E402,F401


def _write_text(path: Path, text: str) -> None:
    """UTF-8 + newline='' — Windows→Linux-container safe."""
    path.parent.mkdir(parents=True, exist_ok=True)
    if not text.endswith("\n"):
        text += "\n"
    path.write_text(text, encoding="utf-8", newline="")


def recompose_from_adrs(
    lib: str,
    spec_dir: Path,
    output_dir: Path,
    provider: str = "anthropic",
    model: str = "claude-sonnet-4-6",
) -> dict:
    """Emit `output_dir/<lib>/` from `spec_dir/<lib>/`.

    Phase 1: writes a single placeholder .py file. Zero LLM calls.
    Returns: {"cost_usd", "elapsed_s", "files_emitted"}.
    """
    t0 = time.time()
    lib_spec = spec_dir / lib  # referenced for symmetry with phase 2
    lib_out = output_dir / lib
    lib_out.mkdir(parents=True, exist_ok=True)

    _write_text(
        lib_out / "__init__.py",
        f'"""Placeholder recompose output for `{lib}`.\n\n'
        "Generated by phase-1 scaffold. Real recompose prompt lands in "
        'phase 2.\n"""\n\n'
        f"# spec source (not read in phase 1): {lib_spec.as_posix()}\n",
    )

    return {
        "cost_usd": 0.0,
        "elapsed_s": round(time.time() - t0, 3),
        "files_emitted": 1,
    }


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument("lib", help="commit0-lite library name (e.g., wcwidth)")
    parser.add_argument(
        "--spec-dir",
        type=Path,
        default=Path(__file__).resolve().parent / "spec",
        help="Root containing <lib>/. Default: benchmarks/round_trip/spec/",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path(__file__).resolve().parent / "recomposed",
        help="Root for recomposed/<lib>/. Default: benchmarks/round_trip/recomposed/",
    )
    parser.add_argument("--provider", choices=["anthropic", "openai"], default="anthropic")
    parser.add_argument("--model", default=None)
    args = parser.parse_args()

    model = args.model or DEFAULT_MODELS[args.provider]
    result = recompose_from_adrs(
        args.lib, args.spec_dir, args.output_dir, args.provider, model
    )
    print(f"recompose {args.lib}: {result}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
